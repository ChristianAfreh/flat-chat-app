  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        leading: null,
        actions: <Widget>[
          IconButton(
              icon: const Icon(Icons.close),
              onPressed: () async {
                //Implement logout functionality
                await _auth.signOut();
                // ignore: use_build_context_synchronously
                Navigator.pop(context);
              }),
        ],
        title: const Text('⚡️Chat'),
